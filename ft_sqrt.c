#include "libft.h"

int	ft_sqrt(int nb)
{
	int a;

	a = 1;
	if (nb < 0)
		return (0);
	while (a * a < nb)
	{
		a++;
		if (a * a == nb)
			return (a);
	}
	return (0);
}
