#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		a;
	size_t	b;

	a = 0;
	b = 0;
	while (s1[a] != '\0')
		a++;
	while (b < n && s2[b] != '\0')
	{
		s1[a] = s2[b];
		a++;
		b++;
	}
	s1[a] = '\0';
	return (s1);
}
